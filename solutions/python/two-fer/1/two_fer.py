def two_fer(name="you"):
    """
    Returns a string in the format "One for <name>, one for me."

    If no name is provided, defaults to "you".

    Parameters:
    name (str): The name of the person to receive the extra cookie. Defaults to "you".

    Returns:
    str: A sentence describing who gets the cookie.
    """
    # Use the given name or default to "you" if no name is provided
    return f"One for {name}, one for me."
